package day04_tasks;

public class PrintDate {

    public static void main(String[] args) {
        int month = 9;
        int day = 7;
        int year = 2023;

        System.out.println(month + "_" + day + "_" + year);
        System.out.println(month + "-" + day + "-" + year);
        System.out.println(month + "|" + day + "|" + year);
        System.out.println(month + "<>" + day + "<>" + year);
    }
}
